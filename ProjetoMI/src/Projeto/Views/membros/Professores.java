    public void carregaTabela(){
        DefaultTableModel modelo = new DefaultTableModel(new Object[]{ "Matricula", "Nome" }, 0 ){
            @Override
            public boolean isCellEditable(int row, int column) { 
                return false;
            }
        };
        
        for( int i=0; i < professor.index().size(); i++){
            modelo.addRow(new Object[]{ 
                professor.index().get(i).getMatricula(), 
                professor.index().get(i).getNome()
            });
        }
        modelo.isCellEditable(0, 0);
        TableProfessor.setModel(modelo);
    }
    
