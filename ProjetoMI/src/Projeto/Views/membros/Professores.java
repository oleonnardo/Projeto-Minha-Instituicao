    private void jbEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbEditarActionPerformed
        int linha = TableProfessor.getSelectedRow();
        
        if( linha>=0 && linha < professor.index().size() ){
            Professor p = professor.search(Integer.valueOf(TableProfessor.getValueAt(linha, 0).toString()));
            tfMatricula.setText( String.valueOf(p.getMatricula()) );
            tfNome.setText(p.getNome());
            modo = "Editar";
            
            jbSalvar.setText("Modificar");
            controleCampos(true);

            jbSalvar.setEnabled(true);
            jbNovo.setEnabled(false);
            jbEditar.setEnabled(true);
            jbRemover.setEnabled(true);
            
        }        
    }//GEN-LAST:event_jbEditarActionPerformed

    private void jbRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbRemoverActionPerformed
        int linha = TableProfessor.getSelectedRow();
        if( linha>=0 && linha < professor.index().size() ){
            if (Flash.confirm("Deseja desmatricular esse professor?", "warning") == 0) {
                if( turma.buscaProfessor(Integer.valueOf(TableProfessor.getValueAt(linha, 0).toString())) ){
                    professor.delete(Integer.valueOf(TableProfessor.getValueAt(linha, 0).toString()));
                    carregaTabela();
                    limpaCampos();
                    jbSalvar.setEnabled(false);
                    jbNovo.setEnabled(true);
                    jbEditar.setEnabled(false);
                    jbRemover.setEnabled(false);
                    controleCampos(false);
                }else{
                    Flash.warning("Professor vinculado a turma(s). Encerre-a primeiro, para remover.");                    
                }
            }
        }
    }//GEN-LAST:event_jbRemoverActionPerformed

    private void jbNovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbNovoActionPerformed
        if( professor.index().size() <= RequisitosFuncionais.quantidadeProfessores ){
            jbSalvar.setEnabled(true);
            jbNovo.setEnabled(false);
            jbEditar.setEnabled(false);
            jbRemover.setEnabled(false);

            controleCampos(true);
            jbSalvar.setText("Matricular");

            limpaCampos();
            modo = "Criar";
            tfMatricula.setText( String.valueOf(Generator.getProximaMatricula()) );
            tfNome.requestFocus();
        }else{
            Flash.warning("Número máximo de professores permitidos.");
        }
    }//GEN-LAST:event_jbNovoActionPerformed

